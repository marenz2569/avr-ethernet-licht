#ifndef ENC28J60_CONFIG_H__
#define ENC28J60_CONFIG_H__

#define ENC28J60_CS_DDR		DDRB
#define ENC28J60_CS_PORT	PORTB
#define ENC28J60_CS_PIN		PORTB2

#define ENC28J60_enable		ENC28J60_CS_PORT &= ~_BV(ENC28J60_CS_PIN) 
#define ENC28J60_disable	ENC28J60_CS_PORT |= _BV(ENC28J60_CS_PIN)

#define ENC28J60_BUFFERSIZE	101

/* 4.5kb receive buffer */
#define ENC28J60_RX_START	0x0000
#define ENC28J60_RX_END		((6 + ENC28J60_MAX_FRAMELEN) * 3 + 1)

/* 0.5kb transmit buffer */
#define ENC28J60_TX_START	2918
#define ENC28J60_TX_END		3019

/* 3kb heap */
#define ENC28J60_HEAP_START	3020
#define ENC28J60_HEAP_END	0x1FFF

#define ENC28J60_MAX_FRAMELEN	966
#define ENC28J60_MAX_DATALEN	(ENC28J60_MAX_FRAMELEN - 60 - 3)

#endif
